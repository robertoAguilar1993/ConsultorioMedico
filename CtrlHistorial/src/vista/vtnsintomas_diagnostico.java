    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        SqlPacientes modsql= new SqlPacientes();
        paciente mod = new paciente();

        mod.setId2(Integer.parseInt(txtid.getText()));
        mod.setDignostico(txadiagnostico.getText());
        mod.setTratamiento(txatratamientos.getText());
        mod.setFecha(fecha.format(date));

        if(modsql.registar_diagnostico(mod)){
            JOptionPane.showMessageDialog(null,"dignostico registrado");
        }
        else{
            JOptionPane.showMessageDialog(null,"no se registro el dignostico");
        }

    }//GEN-LAST:event_btnguardarActionPerformed

