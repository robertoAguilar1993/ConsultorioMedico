    public final void initValores(){
        //jtbPacientes
        String[] titulo = {
            "#",
            "Clave" ,
            "Nombre del paciente",  
            "Domicilio", 
            "Telefono", 
            "Numero de citas",
            "Ultima cita"
        };
        DefaultTableModel modelo = new DefaultTableModel(null, titulo) {
            Class[] types = new Class [] {
                java.lang.Boolean.class, 
                java.lang.Object.class, 
                java.lang.Object.class, 
                java.lang.Object.class,
                java.lang.Object.class, 
                java.lang.Object.class, 
                java.lang.Object.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        };
        
        
        Result<List<AdministracionPacientesVO>> result =
                administracionPacientesController.findAll();
        if(result.isOperationStatus()) {
            Object[] fila = new Object[8];
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            //String fecha = "";
            result.getResult().forEach(adm ->{
                fila[0] = false;
                fila[1] = adm.getId();
                fila[2] = adm.getNombre() + " " + adm.getApellidoPaterno() + " " +
                        adm.getApellidoMaterno();
                fila[3] = adm.getDirecion();
                fila[4] = adm.getTelefono();
                fila[5] = adm.getCitas();
                String fecha = adm.ultimaCita != null ? formatter.format(adm.ultimaCita):"";
                fila[6] = fecha;
                modelo.addRow(fila);
            });
            jtbPacientes.setModel(modelo);
        }else {
            JOptionPane.showMessageDialog(null, result.getMessage());
        }
    }

