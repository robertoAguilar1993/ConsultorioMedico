    public final void getHistorialConsulta(){        
        Result<List<HistorialMtsVO>> result =
                historialPacienteController.getHistorialPacienteByIdPaciente( 
                    (int) pacienteVO.getId()
                );
        
        
        
        int i = 0;
        listHistorial.clear();

        for ( HistorialMtsVO historialMtsVO: result.getResult() ) {
            i++;
            listHistorial.put(String.valueOf(i), historialMtsVO);
        }
        setFechasConsulta();
    }
    
    
    public void setFechasConsulta(){
        jcbFechaConsultas.removeAllItems();
        jcbFechaConsultas.removeAll();
        jcbFechaConsultas.addItem("0" + ": " + "Seleccione un consulta");
        String fecha;
        SimpleDateFormat formatoLargoEsMX = new SimpleDateFormat(
            "EEEE d 'de' MMMM 'de' yyyy", new Locale("ES", "MX")
         );
        for ( Map.Entry<String, HistorialMtsVO> m: listHistorial.entrySet() ) {
            fecha = formatoLargoEsMX.format( m.getValue().getFecha());
             jcbFechaConsultas.addItem( m.getKey()+ ": " + fecha);
        }
        isOperationSearchHistory = true;
    }

