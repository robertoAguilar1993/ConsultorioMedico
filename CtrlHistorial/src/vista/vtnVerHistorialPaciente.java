    private void jcbFechaConsultasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbFechaConsultasItemStateChanged
        // TODO add your handling code here:
       
        if ( jcbFechaConsultas.getItemCount() > 0 && isOperationSearchHistory) {
            String historialSeleccionado = jcbFechaConsultas.getSelectedItem().toString();
            StringTokenizer st = new StringTokenizer(historialSeleccionado, ":");

            if(st.hasMoreElements()){
                 String idHistorialSeleccionado = st.nextElement().toString();

                 if ( !"0".equalsIgnoreCase(idHistorialSeleccionado)){
                    HistorialMtsVO historialMtsVO = listHistorial.get(idHistorialSeleccionado);

                    Result<HistorialMtsData> result = 
                            historialPacienteController.getHistorialPaciente(historialMtsVO);
                    
                    HistorialMtsData historialMtsData =  result.getResult();
                    llenarSintomas(historialMtsData.getSintomasVOList());
                    llenarRecetaMedica(historialMtsData.getRecetaVO());
                    llenarHistorial(historialMtsData.getHistorialVO());
                    llenarReporteUltrasonico(historialMtsData.getReporteUltrasonicoVO());
                 }else{
                     limpiarAntecedentesImportantes();
                     limpiarHistorial();
                     limpiarRecetaMedica();
                     limpiarReporteUltrasonico();
                     limpiarSintomas();
                 }
            }           
        }        
    }//GEN-LAST:event_jcbFechaConsultasItemStateChanged

    @SuppressWarnings("empty-statement")
    public void llenarSintomas(List<SintomasVO> sintomasVOList) {
        String[] titulo = {
            "Sintoma"
        };
        DefaultTableModel modelo = new DefaultTableModel(null, titulo);;
        if (sintomasVOList != null && !sintomasVOList.isEmpty()) {

            Object[] fila = new Object[8];
            for (SintomasVO pacienteVO : sintomasVOList) {
                fila[0] = pacienteVO.getSintomas();
                modelo.addRow(fila);
            }
            jTblSintomas.setModel(modelo);
        }else{
            Object[] fila = new Object[8];
            fila[0] = "No hay sintomas";
            modelo.addRow(fila);
            jTblSintomas.setModel(modelo);
        }
    }
    public void limpiarSintomas() {
        String[] titulo = {
            "Sintoma"
        };
        DefaultTableModel modelo = new DefaultTableModel(null, titulo);;
        Object[] fila = new Object[8];
        fila[0] = "";
        modelo.addRow(fila);
        jTblSintomas.setModel(modelo);
    }
    
    public void llenarRecetaMedica(RecetaVO recetaVO){
        if(recetaVO != null){
            txtEdad.setText(String.valueOf(recetaVO.getEdad()));
            txtPeso.setText(String.valueOf(recetaVO.getPeso()));
            txtTalla.setText(String.valueOf(recetaVO.getTalla()));
            txtTemp.setText(String.valueOf(recetaVO.getTemp()));
            txtFc.setText(String.valueOf(recetaVO.getFc()));
            txtFr.setText(String.valueOf(recetaVO.getRf()));
            txtTa.setText(String.valueOf(recetaVO.getTa()));

            SimpleDateFormat formatoLargoEsMX = new SimpleDateFormat(
                "dd/MM/yyyy"
             );
            String fecha = formatoLargoEsMX.format( recetaVO.getFecha());
            
            txtFecha.setText(String.valueOf(fecha));
            txaRx.setText(String.valueOf(recetaVO.getRx()));
            jDataProximaCita.setDate(recetaVO.getFechaProximaCita());

        }
    }   
    
